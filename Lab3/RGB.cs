namespace Lab3;

public class RGB
{
    public int R_value { get; set; }
    public int G_value { get; set; }
    public int B_value { get; set; }

    public RGB()
    {
    }

    public RGB(int rValue, int gValue, int bValue)
    {
        R_value = rValue;
        G_value = gValue;
        B_value = bValue;
    }
}